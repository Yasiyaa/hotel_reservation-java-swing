    public void setData(String user, String pass) {
        DBConnection obj = new DBConnection();
        ResultSet rst = obj.getUserInformation(user, pass);
        try {

            String fname = null, lname = null, nic = null, add = null, add2 = null, add3 = null, gender = null,
                    tele = null, email = null, password = null, userId = null;

            while (rst.next()) {

                userId = rst.getString("guest_id");
                fname = rst.getString("first_name");
                lname = rst.getString("last_name");
                nic = rst.getString("nic");
                add = rst.getString("address");
                add2 = rst.getString("address2");
                add3 = rst.getString("address3");
                gender = rst.getString("gender");
                tele = rst.getString("telephone");
                email = rst.getString("email");
                password = rst.getString("password");
            }
            txt_firstname.setText(fname);
            txt_lastname.setText(lname);
            txt_nic.setText(nic);
            txt_add1.setText(add);
            txt_add2.setText(add2);
            txt_add3.setText(add3);
            txt_contact.setText(tele);
            txt_email.setText(email);
            txt_pass.setText(password);

            if (gender.equalsIgnoreCase("male")) {
                rbtn_male.setSelected(true);
            } else {
                rbtn_female.setSelected(true);
            }

            Loginform.reservationObj.setGuestID(userId);
            Loginform.reservationDetails.setUserID(userId);
            Loginform.hallReservation.setGuestID(userId);
            Loginform.HallreservationDetails.setUserID(userId);
            Loginform.FeedbackObj.setUserData(userId, fname);

        } catch (SQLException ex) {
            Logger.getLogger(UserManageform.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

