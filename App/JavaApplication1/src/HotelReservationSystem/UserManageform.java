    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        String userId = Loginform.reservationObj.guestID;
        String fname = txt_firstname.getText();
        String lname = txt_lastname.getText();
        String nic = txt_nic.getText();
        String add = txt_add1.getText();
        String add2 = txt_add2.getText();
        String add3 = txt_add3.getText();
        String tele = txt_contact.getText();
        String email = txt_email.getText();
        String password = txt_pass.getText();
        String conf = txt_confpass.getText();

        if (fname.isEmpty()) {
            JOptionPane.showMessageDialog(null, "First name cannot be empty.", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (lname.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Last name cannot be empty.", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (email.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Email cannot be empty.", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (password.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Password cannot be empty.", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (conf.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Password is not matching.", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (nic.isEmpty()) {
            JOptionPane.showMessageDialog(null, "NIC cannot be empty.", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (add.isEmpty() || add2.isEmpty() || add3.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Address  cannot be empty.", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (tele.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Telephone cannot be empty.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {

            if (password.equalsIgnoreCase(conf)) {
                try {
                    Statement st = HotelReservationSystem.DBConnection.creatConnection().createStatement();
                    String query = "UPDATE `customer` SET `first_name` = '" + fname + "', `last_name` = '" + lname + "', `nic` = '" + nic + "', `address` = '" + add + "', `address2` = '" + add2 + "', `address3` = '" + add3 + "', `password` = '" + password + "', `email` = '" + email + "', `telephone` = '" + tele + "' WHERE `customer`.`guest_id` = '" + userId + "';";
                    int count = st.executeUpdate(query);
                    System.out.println(query);
                    if (count > 0) {
                        JOptionPane.showMessageDialog(null, "Profile updated successfully completed.", "Success", JOptionPane.INFORMATION_MESSAGE);

                    }

                } catch (Exception e) {
                }
            } else {
                JOptionPane.showMessageDialog(null, "Password is not matching.", "Error", JOptionPane.ERROR_MESSAGE);
            }

        }


    }//GEN-LAST:event_btn_updateActionPerformed

