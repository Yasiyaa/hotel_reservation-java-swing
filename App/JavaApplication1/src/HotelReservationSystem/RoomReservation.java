    public void getRoomId() {
        try {
            Statement st = HotelReservationSystem.DBConnection.creatConnection().createStatement();
            ResultSet rst = st.executeQuery("Select * from room_tbl where room_status = 'true'");
            while (rst.next()) {
                cmb_roomno.addItem(rst.getString("roomID"));
            }
        } catch (SQLException ex) {

        }

    }

