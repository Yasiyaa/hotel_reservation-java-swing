    private void btn_bookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_bookActionPerformed
//        System.out.println(guestID);

        String checkInDate, checkOutDate, requests, roomid;
        int no_adult, no_child;

        requests = txt_req.getText();
        roomid = cmb_roomno.getSelectedItem().toString();

        SimpleDateFormat sfd = new SimpleDateFormat("yyyy-MM-dd");
        checkInDate = sfd.format(datepicker_checkin_date.getDate());
        checkOutDate = sfd.format(datepicker_checkout_date.getDate());

        no_adult = Integer.parseInt(txt_no_of_adults.getText());
        no_child = Integer.parseInt(txt_no_of_child.getText());
        
 

        try {
            Statement st = HotelReservationSystem.DBConnection.creatConnection().createStatement();
            String query = "INSERT INTO `room_reservation` (`roomID`, `guestID`,`check_in_date`, `check_out_date`, `number_of_adults`, `number_of_children`, `special_requests`) VALUES ( '" + roomid + "','" + guestID + "', '" + checkInDate + "', '" + checkOutDate + "', '" + no_adult + "', '" + no_child + "', '" + requests + "');";
            int count = st.executeUpdate(query);
            //System.out.println(query);
            if (count > 0) {
                JOptionPane.showMessageDialog(null, "Reservation successfully completed.", "Success", JOptionPane.INFORMATION_MESSAGE);

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Something went wrong !","Error",JOptionPane.ERROR_MESSAGE);
        }
        


    }//GEN-LAST:event_btn_bookActionPerformed

